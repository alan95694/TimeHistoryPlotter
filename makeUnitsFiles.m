function makeFile(info, folder)

info{2} = strrep(info{2}, '/', '_per_');
info{4} = strrep(info{4}, '/', '_per_');


fileName = [info{2}, '_To_', info{4}];

% Open or create a text file for writing
fileID = fopen(fullfile(folder, [fileName, '.m']), 'w');

% Check if the file was opened successfully
if fileID == -1
    error('Cannot open file for writing.');
end

% Write some text to the file
fprintf(fileID, ['function out = ', fileName, '(inp)\n']);
fprintf(fileID, ['%% ' info{1}, ' to ', info{3}, ', k = ', num2str(info{5}), '\n']);
fprintf(fileID, '\n');
fprintf(fileID, ['out = ', num2str(info{5}), '.*inp;\n']);
fprintf(fileID, 'end\n');

% Close the file
fclose(fileID);

end
